  ADD COLUMN IF NOT EXISTS benefits TEXT[] DEFAULT ARRAY[]::TEXT[],
  ADD COLUMN IF NOT EXISTS key_features TEXT[] DEFAULT ARRAY[]::TEXT[];

ALTER TABLE public.products
  ADD COLUMN IF NOT EXISTS long_description TEXT;

