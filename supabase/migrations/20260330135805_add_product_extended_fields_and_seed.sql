  # Add Extended Product Fields + Seed All Products

  1. Schema Changes
    - Add `benefits` (TEXT[]) column to products
    - Add `key_features` (TEXT[]) column to products
    - Add UNIQUE constraint on `sku` for upsert safety
    - Change `id` to accept string-type SKU values (already TEXT PRIMARY KEY)

  2. Seed Data
    - Insert all 34 CoreForge products
    - Uses ON CONFLICT (sku) DO NOTHING for idempotency

  3. Notes
    - Products use TEXT primary keys matching their SKU codes (CFG-001 etc)
    - Image paths reference local /other/supp-images/ folder
*/

